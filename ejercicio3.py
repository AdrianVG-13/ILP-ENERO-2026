# 3. Determinar la edad de una persona conociendo el año actual y el año de nacimiento

# ENTRADA DE DATOS
# Declaración o creación de variables
año_actual = int(input("Ingresa el año actual: "))
año_nacimiento = int(input("Ingresa tu año de nacimiento: "))
# PROCESOS (Cálculos u operaciones matemáticas y/o lógicas)
edad = año_actual - año_nacimiento  
# SALIDA DE DATOS (Resultados)
print("Tu edad es:", edad)
